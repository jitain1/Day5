package com.bridgelabz.Basics;

public class Swapping {

	public static void main(String[] args) {

		int i = 10;
		int j = 15;
		int m;

		System.out.println("i =" + i);
		System.out.println("j =" + j);

		m = i;
		i = j;
		j = m;
		System.out.println("The Values after Swapping : ");
		System.out.println("i =" + i);
		System.out.println("j =" + j);

	}

}
